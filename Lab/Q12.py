my_tuple = (10, 20, 10, 40, 50, 30)


print("Length of tuple:", len(my_tuple))

print("Count of 10:", my_tuple.count(10))

print("Index of 40:", my_tuple.index(40))

print("Sorted tuple:", sorted(my_tuple))

print("Minimum element:", min(my_tuple))

print("Maximum element:", max(my_tuple))

print("Reversed tuple:", tuple(reversed(my_tuple)))

